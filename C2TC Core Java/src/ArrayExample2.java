public class ArrayExample2 {

	public static void main(String[] args) {
		
		int[] arr = new int[5];
		arr[0] = 15;
		arr[1] = 25;
		arr[2] = 35;
		arr[3] = 45;
		arr[4] = 55;
		
		for(int i=0; i<arr.length; i++) {
			
			System.out.print(arr[i] + "   ");
			
		}
		System.out.println();

	}

}